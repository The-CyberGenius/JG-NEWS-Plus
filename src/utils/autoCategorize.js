/**
 * Score how strongly a piece of text matches each category, return the best match.
 * @param {string} text — title + content (Devanagari)
 * @param {string[]} availableCategories — categories defined in DB; only these are considered
 * @param {object} options — { minScore: number, fallback: string|null }
 * @returns {string|null} matched category name, or fallback/null
 */
export function autoCategorize(text, availableCategories = [], { minScore = 2, fallback = null } = {}) {
    if (!text || typeof text !== 'string') return fallback;
    const lower = text.toLowerCase();

    const scores = {};
    for (const [cat, words] of Object.entries(KEYWORDS)) {
        if (availableCategories.length > 0 && !availableCategories.includes(cat)) continue;
        let s = 0;
        for (const w of words) {
            // Hindi/Devanagari is case-insensitive already, but lowercase helps the English keywords
            if (lower.includes(w.toLowerCase())) s++;
        }
        if (s > 0) scores[cat] = s;
    }

    const entries = Object.entries(scores).sort((a, b) => b[1] - a[1]);
    if (entries.length === 0) return fallback;
    const [topCat, topScore] = entries[0];
    if (topScore < minScore) return fallback;
    return topCat;
}

